//normal finction
const square = function (x) {
    return x * x; 
}
console.log(square(12));




//arrow method
const borgo = (x) =>  x * x;
console.log(borgo(13));




const isEven = x => (x % 2 === 0)
console.log(isEven(3));




const add = function (q, b) {
    return q + b;  
}
console.log(add(123, 1234));




const add2 = (q, b) => q + b;  
console.log(add2(123, 234));
